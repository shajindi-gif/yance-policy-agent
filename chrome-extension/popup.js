/**
 * Detect if the current page is likely a government policy page.
 */
function isGovernmentPolicyPage(text, url, title) {
  const govUrlPatterns = /gov\.cn|\.gov\.|\.edu\.cn|gov\.org|zhengce|zhengfu/;
  const policyKeywords = [
    '政策', '补贴', '扶持', '奖励', '资助', '申报', '通知',
    '意见', '办法', '规定', '高新技术', '专精特新', '科技型中小',
    '产业园', '开发区', '高新区', '经开区', '示范区',
    '关于', '印发', '实施', '若干措施', '若干政策',
  ];

  const urlMatch = govUrlPatterns.test(url);
  const keywordCount = policyKeywords.filter((kw) => text.includes(kw)).length;

  return urlMatch || keywordCount >= 3;
}

/**
 * Analyze extracted page text for policy-like content.
 * Returns a structured object with the 6 required fields.
 */
function analyzePolicyContent(text, url, title) {
  if (!text || text.length < 50) {
    return getMockAnalysis();
  }

  if (isGovernmentPolicyPage(text, url, title) && text.length > 100) {
    return extractFromRealContent(text, url, title);
  }

  return getMockAnalysis();
}

/**
 * Extract structured policy data from real page content.
 * Returns the 6-field structure.
 */
function extractFromRealContent(text, url, title) {
  // 1. policy_name
  let policy_name = title || '待确认';
  const titlePatterns = [
    /关于[\u4e00-\u9fa5]+(?:的通知|的意见|的办法|的方案|的措施)/,
    /[\u4e00-\u9fa5]+(?:扶持政策|支持政策|发展政策|专项资金)/,
  ];
  for (const pattern of titlePatterns) {
    const match = text.match(pattern);
    if (match) {
      policy_name = match[0];
      break;
    }
  }

  // 2. issuing_authority
  let issuing_authority = '待确认';
  const authorityPatterns = [
    /([\u4e00-\u9fa5]+(?:人民政府|管委会|科技局|发改委|经信委|工信局|科委))/,
    /([\u4e00-\u9fa5]+(?:办公室|财政厅|人社厅|商务厅))/,
  ];
  for (const pattern of authorityPatterns) {
    const match = text.match(pattern);
    if (match) {
      issuing_authority = match[1];
      break;
    }
  }

  // 3. target_industry
  const target_industry = [];
  const industryMap = {
    '人工智能': /人工智能|AI|大模型|机器学习|深度学习/,
    '生物医药': /生物医药|医药|生物技术|创新药|医疗器械/,
    '集成电路': /集成电路|芯片|半导体|微电子/,
    '新材料': /新材料|先进材料|功能材料/,
    '新能源': /新能源|清洁能源|光伏|储能|风电/,
    '智能制造': /智能制造|机器人|自动化|工业互联网/,
    '软件与信息服务': /软件|信息服务|数字|数据|云计算/,
  };
  for (const [industry, pattern] of Object.entries(industryMap)) {
    if (pattern.test(text)) {
      target_industry.push(industry);
    }
  }
  if (target_industry.length === 0) target_industry.push('科技型企业');

  // 4. subsidy_amount
  let subsidy_amount = '详见政策原文';
  const subsidyPatterns = [
    /(?:最高|不超过)\s*([\d,]+)\s*万/,
    /补贴\s*([\d,]+)\s*万/,
    /奖励\s*([\d,]+)\s*万/,
    /资助\s*([\d,]+)\s*万/,
  ];
  for (const pattern of subsidyPatterns) {
    const match = text.match(pattern);
    if (match) {
      subsidy_amount = `最高 ${match[1]} 万元`;
      break;
    }
  }

  // 5. application_deadline
  let application_deadline = '详见政策原文';
  const deadlinePatterns = [
    /(?:截止|截至|申报期限)[：:]*\s*(\d{4}年\d{1,2}月\d{1,2}日)/,
    /(\d{4}年\d{1,2}月\d{1,2}日)(?:前|之前)/,
    /(\d{4}年\d{1,2}月)/,
  ];
  for (const pattern of deadlinePatterns) {
    const match = text.match(pattern);
    if (match) {
      application_deadline = match[1];
      break;
    }
  }

  // 6. material_requirements
  const material_requirements = [
    '企业营业执照副本（复印件加盖公章）',
    '企业近年度审计报告或财务报表',
    '相关知识产权证明材料',
    '项目申报书或申请表',
    '企业社保缴纳证明',
    '其他相关佐证材料',
  ];

  return {
    policy_name,
    issuing_authority,
    target_industry,
    subsidy_amount,
    application_deadline,
    material_requirements,
  };
}

