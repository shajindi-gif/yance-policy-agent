/**
 * Generate a concise service note from analysis.
 */
function generateServiceNote(analysis) {
  const noteSuggestions = analysis.serviceSuggestions.map((s) => ({
    title: s.title,
    desc: `[服务备注] ${s.desc}`,
  }));

  return {
    ...analysis,
    summary: `[园区服务备注]\n${analysis.summary}`,
    serviceSuggestions: noteSuggestions,
  };
}

