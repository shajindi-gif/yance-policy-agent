const resultsEl = document.getElementById('results');

// Store the current analysis for ParkOps copy
let currentAnalysis = null;

// ============================================
// Event Listeners
// ============================================
btnAnalyze.addEventListener('click', handleAnalyze);
btnServiceNote.addEventListener('click', handleServiceNote);
btnCopyParkOps.addEventListener('click', handleCopyParkOps);
btnOpenDashboard.addEventListener('click', handleOpenDashboard);

// ============================================
// Main Handlers
