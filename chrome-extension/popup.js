/**
 * Analyze the current page for policy content.
 */
async function handleAnalyze() {
  disableButtons(true);
  showLoading(true);
  hideResults();

  try {
    const [tab] = await chrome.tabs.query({ active: true, currentWindow: true });

    if (!tab || !tab.id) {
      throw new Error('无法获取当前标签页');
    }

    const pageText = await extractPageText(tab.id);
    const analysis = analyzePolicyContent(pageText, tab.url, tab.title);

    currentAnalysis = analysis;
    renderAnalysisResults(analysis);
    showResults();
  } catch (error) {
    console.error('Analysis error:', error);
    // Fall back to mock analysis on any error
    const mockAnalysis = getMockAnalysis();
    currentAnalysis = mockAnalysis;
    renderAnalysisResults(mockAnalysis);
    showResults();
  } finally {
    showLoading(false);
    disableButtons(false);
  }
}

/**
 * Generate a park service ticket based on current page analysis.
 */
async function handleServiceNote() {
  disableButtons(true);
  showLoading(true);
  hideResults();

  try {
    const [tab] = await chrome.tabs.query({ active: true, currentWindow: true });
    const pageText = tab ? await extractPageText(tab.id) : '';
    const analysis = analyzePolicyContent(pageText, tab ? tab.url : '', tab ? tab.title : '');

    currentAnalysis = analysis;
    renderAnalysisResults(analysis);
    showResults();
  } catch (error) {
    console.error('Service note error:', error);
    const mockAnalysis = getMockAnalysis();
    currentAnalysis = mockAnalysis;
    renderAnalysisResults(mockAnalysis);
    showResults();
  } finally {
    showLoading(false);
    disableButtons(false);
  }
}

/**
 * Copy structured analysis as a ParkOps service ticket to clipboard.
 */
function handleCopyParkOps() {
  if (!currentAnalysis) {
    return;
  }

  const a = currentAnalysis;
  const now = new Date();
  const ticketId = `SVC-${now.getFullYear()}${String(now.getMonth() + 1).padStart(2, '0')}${String(now.getDate()).padStart(2, '0')}-${String(now.getHours()).padStart(2, '0')}${String(now.getMinutes()).padStart(2, '0')}`;

  const ticket = [
    `═══════════════════════════════════════`,
    `  ParkOps 服务工单  |  yance.ai`,
    `═══════════════════════════════════════`,
    ``,
    `工单编号：${ticketId}`,
    `创建时间：${now.toLocaleString('zh-CN')}`,
    `来源工具：YanCe Policy Agent Chrome Extension`,
    ``,
    `── 政策信息 ──`,
    `政策名称：${a.policy_name || '待确认'}`,
    `发文机构：${a.issuing_authority || '待确认'}`,
    `适用行业：${(a.target_industry || []).join('、') || '待确认'}`,
    `补贴金额：${a.subsidy_amount || '待确认'}`,
    `申报期限：${a.application_deadline || '待确认'}`,
    ``,
    `── 材料要求 ──`,
    ...(a.material_requirements || []).map((m, i) => `  ${i + 1}. ${m}`),
    ``,
    `═══════════════════════════════════════`,
    `  Generated by yance.ai | YanCe Policy Agent`,
    `═══════════════════════════════════════`,
  ].join('\n');

  navigator.clipboard.writeText(ticket).then(() => {
    const origHTML = btnCopyParkOps.innerHTML;
    btnCopyParkOps.textContent = '已复制到剪贴板';
    btnCopyParkOps.disabled = true;
    setTimeout(() => {
      btnCopyParkOps.innerHTML = origHTML;
      btnCopyParkOps.disabled = false;
    }, 2000);
  }).catch(() => {
    alert('复制失败，请手动复制');
  });
}

