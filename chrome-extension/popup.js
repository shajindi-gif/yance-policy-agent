/**
 * Detect if the current page is likely a government policy page.
 * @param {string} text
 * @param {string} url
 * @param {string} title
 * @returns {boolean}
 */
function isGovernmentPolicyPage(text, url, title) {
  const govUrlPatterns = /gov\.cn|\.gov\.|\.edu\.cn|gov\.org|zhengce|zhengfu|weiyuanhui/;
  const policyKeywords = [
    '政策', '补贴', '扶持', '奖励', '资助', '申报', '通知',
    '意见', '办法', '规定', '高新技术', '专精特新', '科技型中小',
    '产业园', '开发区', '高新区', '经开区', '示范区',
    '关于', '印发', '实施', '若干措施', '若干政策',
  ];

  const urlMatch = govUrlPatterns.test(url);
  const keywordCount = policyKeywords.filter((kw) => text.includes(kw)).length;

  return urlMatch || keywordCount >= 3;
}

/**
 * Analyze extracted page text for policy-like content.
 * @param {string} text
 * @param {string} url
 * @param {string} title
 * @returns {object} Structured analysis
 */
function analyzePolicyContent(text, url, title) {
  if (!text || text.length < 50) {
    return getMockAnalysis();
  }

  const isPolicyPage = isGovernmentPolicyPage(text, url, title);

  if (isPolicyPage && text.length > 100) {
    return extractFromRealContent(text, url, title);
  }

  return getMockAnalysis();
}

/**
 * Extract structured policy data from real page content.
 */
function extractFromRealContent(text, url, title) {
  // Detect region
  const regionPatterns = {
    '上海市浦东新区': /上海|沪|浦东新区|张江|临港|漕河泾/,
    '上海市徐汇区': /徐汇/,
    '上海市杨浦区': /杨浦/,
    '北京市中关村': /北京|京|中关村|亦庄|经开区/,
    '深圳市南山区': /深圳|粤|南山|前海|福田/,
    '杭州市余杭区': /杭州|浙|余杭|滨江|未来科技城/,
    '苏州市工业园区': /苏州|苏|工业园区|昆山/,
    '成都市天府新区': /成都|川|天府新区/,
  };

  let detectedRegion = '待确认';
  for (const [region, pattern] of Object.entries(regionPatterns)) {
    if (pattern.test(text) || pattern.test(url)) {
      detectedRegion = region;
      break;
    }
  }

  // Detect issuing authority
  const authorityPatterns = [
    /([\u4e00-\u9fa5]+(?:人民政府|管委会|科技局|发改委|经信委|工信局|科委))/,
    /([\u4e00-\u9fa5]+(?:办公室|财政厅|人社厅|商务厅))/,
  ];
  let issuingAuthority = '待确认';
  for (const pattern of authorityPatterns) {
    const match = text.match(pattern);
    if (match) {
      issuingAuthority = match[1];
      break;
    }
  }

  // Detect policy name from title or first heading
  let policyName = title || '待确认';
  const titlePatterns = [
    /关于[\u4e00-\u9fa5]+(?:的通知|的意见|的办法|的方案|的措施)/,
    /[\u4e00-\u9fa5]+(?:扶持政策|支持政策|发展政策|专项资金)/,
  ];
  for (const pattern of titlePatterns) {
    const match = text.match(pattern);
    if (match) {
      policyName = match[0];
      break;
    }
  }

  // Detect target industries
  const targetIndustries = [];
  const industryMap = {
    '人工智能': /人工智能|AI|大模型|机器学习|深度学习/,
    '生物医药': /生物医药|医药|生物技术|创新药|医疗器械/,
    '集成电路': /集成电路|芯片|半导体|微电子/,
    '新材料': /新材料|先进材料|功能材料/,
    '新能源': /新能源|清洁能源|光伏|储能|风电/,
    '智能制造': /智能制造|机器人|自动化|工业互联网/,
    '软件与信息服务': /软件|信息服务|数字|数据|云计算/,
  };
  for (const [industry, pattern] of Object.entries(industryMap)) {
    if (pattern.test(text)) {
      targetIndustries.push(industry);
    }
  }
  if (targetIndustries.length === 0) targetIndustries.push('科技型企业');

  // Detect enterprise types
  const enterpriseTypes = [];
  if (/高新技术|高企/.test(text)) enterpriseTypes.push('高新技术企业');
  if (/专精特新/.test(text)) enterpriseTypes.push('专精特新企业');
  if (/科技型中小/.test(text)) enterpriseTypes.push('科技型中小企业');
  if (/小微/.test(text)) enterpriseTypes.push('小微企业');
  if (/独角兽/.test(text)) enterpriseTypes.push('独角兽企业');
  if (/瞪羚/.test(text)) enterpriseTypes.push('瞪羚企业');
  if (enterpriseTypes.length === 0) enterpriseTypes.push('科技型企业', '中小企业');

  // Detect subsidy amount
  let subsidyAmount = '详见政策原文';
  const subsidyPatterns = [
    /(?:最高|不超过)\s*([\d,]+)\s*万/,
    /补贴\s*([\d,]+)\s*万/,
    /奖励\s*([\d,]+)\s*万/,
    /资助\s*([\d,]+)\s*万/,
  ];
  for (const pattern of subsidyPatterns) {
    const match = text.match(pattern);
    if (match) {
      subsidyAmount = `最高 ${match[1]} 万元`;
      break;
    }
  }

  // Detect deadline
  let deadline = '详见政策原文';
  const deadlinePatterns = [
    /(?:截止|截至|申报期限)[：:]*\s*(\d{4}年\d{1,2}月\d{1,2}日)/,
    /(\d{4}年\d{1,2}月\d{1,2}日)(?:前|之前)/,
    /(\d{4}年\d{1,2}月)/,
  ];
  for (const pattern of deadlinePatterns) {
    const match = text.match(pattern);
    if (match) {
      deadline = match[1];
      break;
    }
  }

  // Extract summary (first meaningful paragraphs)
  const paragraphs = text.split(/\n+/).filter((p) => p.trim().length > 20);
  const summary = paragraphs.slice(0, 3).join('\n').substring(0, 500) || text.substring(0, 500);

  return {
    policyName: policyName,
    issuingAuthority: issuingAuthority,
    summary: summary,
    region: detectedRegion,
    targetIndustries: targetIndustries,
    subsidyAmount: subsidyAmount,
    deadline: deadline,
    enterpriseTypes: enterpriseTypes,
    conditions: [
      '在该区域注册并实际经营的独立法人企业',
      '符合国家及地方产业发展方向',
      '企业经营状况良好，无严重违法违规记录',
      '具备相关领域的核心技术或业务能力',
      '按规定提交完整的申报材料',
    ],
    materials: [
      '企业营业执照副本（复印件加盖公章）',
      '企业近年度审计报告或财务报表',
      '相关知识产权证明材料',
      '项目申报书或申请表',
      '企业社保缴纳证明',
      '其他相关佐证材料',
    ],
    riskTips: [
      '请注意申报截止时间，提前准备材料',
      '确保所有材料真实有效，虚假信息将被取消资格',
      '部分政策存在名额限制，建议尽早申报',
      '关注政策是否有配套的地方实施细则',
    ],
    serviceSuggestions: [
      {
        title: '主动通知相关企业',
        desc: '筛选园区内符合条件的企业，主动推送政策信息',
      },
      {
        title: '组织政策解读会',
        desc: '邀请政策制定部门或专业机构进行解读，帮助企业理解政策要点',
      },
      {
        title: '提供申报辅导',
        desc: '协助企业准备申报材料，提高申报成功率',
      },
      {
        title: '建立跟踪机制',
        desc: '跟踪企业申报进度，及时反馈并协助解决问题',
      },
    ],
    nextSteps: [
      '将政策要点整理归档至园区政策库',
      '筛选符合条件的园区企业名单',
      '制作政策摘要推送给目标企业',
      '安排专人跟进企业申报进度',
      '定期汇总政策落地情况并上报',
    ],
  };
}

