/**
 * Render analysis results into the DOM.
 * @param {object} analysis
 */
function renderAnalysisResults(analysis) {
  // Policy Name
  const policyNameEl = document.getElementById('policy-name');
  if (policyNameEl) {
    policyNameEl.textContent = analysis.policyName || '待确认';
  }

  // Issuing Authority
  const authEl = document.getElementById('issuing-authority');
  if (authEl) {
    authEl.textContent = analysis.issuingAuthority || '待确认';
  }

  // Summary
  document.getElementById('policy-summary').textContent = analysis.summary;

  // Region
  document.getElementById('applicable-region').textContent = analysis.region;

  // Target Industries (as tags)
  const industryContainer = document.getElementById('target-industry');
  if (industryContainer) {
    industryContainer.innerHTML = '';
    (analysis.targetIndustries || []).forEach((ind) => {
      const tag = document.createElement('span');
      tag.className = 'tag tag-coral';
      tag.textContent = ind;
      industryContainer.appendChild(tag);
    });
  }

  // Subsidy Amount
  const subsidyEl = document.getElementById('subsidy-amount');
  if (subsidyEl) {
    subsidyEl.textContent = analysis.subsidyAmount || '详见政策原文';
  }

  // Deadline
  const deadlineEl = document.getElementById('application-deadline');
  if (deadlineEl) {
    deadlineEl.textContent = analysis.deadline || '详见政策原文';
  }

  // Enterprise Types (as tags)
  const typesContainer = document.getElementById('enterprise-types');
  typesContainer.innerHTML = '';
  analysis.enterpriseTypes.forEach((type) => {
    const tag = document.createElement('span');
    tag.className = 'tag';
    tag.textContent = type;
    typesContainer.appendChild(tag);
  });

  // Conditions
  const conditionsList = document.getElementById('application-conditions');
  conditionsList.innerHTML = '';
  analysis.conditions.forEach((cond) => {
    const li = document.createElement('li');
    li.textContent = cond;
    conditionsList.appendChild(li);
  });

  // Materials
  const materialsList = document.getElementById('material-list');
  materialsList.innerHTML = '';
  analysis.materials.forEach((mat) => {
    const li = document.createElement('li');
    li.textContent = mat;
    materialsList.appendChild(li);
  });

  // Risk Tips
  const riskList = document.getElementById('risk-tips');
  riskList.innerHTML = '';
  analysis.riskTips.forEach((tip) => {
    const li = document.createElement('li');
    li.textContent = tip;
    riskList.appendChild(li);
  });

  // Service Suggestions
  const suggestionsContainer = document.getElementById('service-suggestions');
  suggestionsContainer.innerHTML = '';
  analysis.serviceSuggestions.forEach((suggestion) => {
    const div = document.createElement('div');
    div.style.marginBottom = '8px';
    div.innerHTML = `
      <strong style="color: #7dd3fc; font-size: 12px;">${suggestion.title}</strong>
      <p style="margin-top: 2px; font-size: 12px; color: #cbd5e1;">${suggestion.desc}</p>
    `;
    suggestionsContainer.appendChild(div);
  });

  // Next Steps
  const stepsList = document.getElementById('next-steps');
  stepsList.innerHTML = '';
  analysis.nextSteps.forEach((step) => {
    const li = document.createElement('li');
    li.textContent = step;
    stepsList.appendChild(li);
  });
}

